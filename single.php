          <h1 class="post-title">Why does it hurt so much when we get ghosted? A psychologist explains</h1>

          <div class="post-content">
            <p>As humans, we spend a good amount of time thinking about what other people think.
            </p>
            <p>Although this is not meant to be a controversial proposition, I am sure some of you are saying to yourselves, “No, I don’t do that,” or “I don’t care what other people think.”</p>
            <p>This is understandable. Any opposition you might feel to this statement may stem from well-meaning advice givers, who in an effort to direct you away from self-criticism and anxiety, have assured you that what other people think of you does not matter.</p>
            <p>To that I will say simply, “Yes, it does,” and urge you to read on as I explain why.

</p>
            <p>I’ll begin with a statement that we should all be able to endorse easily: When we act, it is likely and appropriate that we would think about how our actions affect others. For example, we think, “What does X think about what I just said?” Still, our thinking about others’ thoughts can also get more complex. We can widen our scope of inquiry by focusing on what someone thinks about the way we feel about someone else (e.g., “What does Y think I feel about X, considering what I just said?” or “What does Y think X feels about me, considering what I just said?”).
            </p>
            <p>These are not necessarily negative or maladaptive thoughts.</p>
            <p>Quite to the contrary, these are complex ways of thinking — employed by the most socially skilled among us — to help us develop and maintain social connections. Although important, this way of thinking is not inborn; it is a sophisticated and critical skill we hone over our lifetimes.</p>
            <p>As toddlers, we don’t consider our effects on other people. We make demands, cry and throw tantrums in an egocentric way, wanting only to satisfy our own needs and not caring about anyone else. It is only as we learn more about the world, when we develop something called “theory of mind” between the ages of about three to five, that we begin to recognize that other people have thoughts and feelings, and that our actions might affect them. By definition, the theory of mind is an ability to understand that both you and others have mental states and that others’ mental states might be different from your own and even different from what is reality.</p>
            <p>This understanding of one’s own mental states gives individuals great power because once you realize you have thoughts and feelings, you can learn to control those thoughts and feelings. One of my favorite stories about the power that this skill affords comes from one of my university colleagues and a conversation he had with his daughter while driving her to preschool:
            </p>
            <p>“Dad, do you know what I’m thinking about?”</p>
            <p>“No, Claire, what are you thinking about?”</p>
            <p>“I’m thinking about Christmas.”</p>
            <p>As a preschooler, Claire not only showed her dad that she had theory of mind but also demonstrated an impressive mastery of directing those thoughts to control her own internal world.
            </p>
            <p>I contend that this is the skill that the advice givers are trying to get you to develop. Essentially, they are not saying, “Don’t think about what other people think,” which would be almost impossible for those of us over the age of five who have even a bit of empathy. Rather, they are saying, “When you think about what others think, use it for its potential good and then dispose of it,” in a Marie Kondo kind of way.</p>
            <p>Considering the average human has more than 6,000 thoughts per day, this type of cognitive purging would likely do us good, freeing up our mental resources for more positive thinking. But just like ridding our homes of unwanted items is a challenge for many of us, so is ridding our brains of unwanted thoughts and feelings. Particularly for those prone to anxiety and depression, worry and rumination — or stewing on negative thoughts associated with past events — is common and maladaptive.</p>
            
